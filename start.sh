    echo -e "${YELLOW}‚ö† –í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –±—ç–∫–µ–Ω–¥–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ${NC}"
    echo -e "${YELLOW}  –°–æ–∑–¥–∞–π—Ç–µ –µ–≥–æ –∫–æ–º–∞–Ω–¥–æ–π: python3 -m venv backend/venv${NC}"
