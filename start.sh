# –ó–∞–ø—É—Å–∫ –±—ç–∫–µ–Ω–¥–∞
echo -e "${BLUE}üì¶ –ó–∞–ø—É—Å–∫ –±—ç–∫–µ–Ω–¥–∞ (FastAPI)...${NC}"
