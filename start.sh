if [ -d "backend/venv" ]; then
    echo -e "${GREEN}‚úì –ê–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è –±—ç–∫–µ–Ω–¥–∞${NC}"
    source backend/venv/bin/activate
